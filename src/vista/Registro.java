    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
   int identificacion = Integer.parseInt(txtIdentificacion.getText());
                String nombre = txtNombreUsuario.getText();
                String email = txtEmail.getText();
                String clave = new String(txtClave.getPassword());
                String confirmarClave = new String(txtClave2.getPassword());

                if (!clave.equals(confirmarClave)) {
                    JOptionPane.showMessageDialog(null, "Las claves no coinciden");
                    return;
                }

                Usuario nuevoUsuario = new Usuario(identificacion, nombre, email, clave, "Usuario");
                if (archivos != null) {
        archivos.registrarUsuario(nuevoUsuario);
        JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente");
    } else {
        // Maneja el caso en que archivos sea null
        JOptionPane.showMessageDialog(null, "Error: No se puede registrar el usuario. El sistema de archivos no est√° inicializado.", "Error", JOptionPane.ERROR_MESSAGE);
    }
             
       
    }//GEN-LAST:event_btnRegistroActionPerformed

